    private void txt_calcuKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_calcuKeyReleased
        // TODO add your handling code here:
        char tecla=evt.getKeyChar();
        
        if(tecla==KeyEvent.VK_MINUS)
        {
            btn_resta.doClick();
        }
        else if(tecla=='+')
        {
            btn_suma.doClick();
        }
        else if(tecla=='*')
        {
            btn_multi.doClick();
        }
        else if(tecla=='/')
        {
            btn_division.doClick();
        }
        else if(tecla=='='||tecla==KeyEvent.VK_ENTER) //Se va a acceder al boton igual con = o con la tecla enter
        {
            btn_igual.doClick();
        }
        else if(tecla == KeyEvent.VK_1)
        {
            btn_uno.doClick();
        }
        else if(tecla == KeyEvent.VK_2)
        {
            btn_dos.doClick();
        }
        else if(tecla == KeyEvent.VK_3)
        {
            btn_tres.doClick();
        }
        else if(tecla == KeyEvent.VK_4)
        {
            btn_cuatro.doClick();
        }
        else if(tecla == KeyEvent.VK_5)
        {
            btn_cinco.doClick();
        }
        else if(tecla == KeyEvent.VK_6)
        {
            btn_seis.doClick();
        }
        else if(tecla == KeyEvent.VK_7)
        {
            btn_siete.doClick();
        }
        else if(tecla == KeyEvent.VK_8)
        {
            btn_ocho.doClick();
        }
        else if(tecla == KeyEvent.VK_9)
        {
            btn_nueve.doClick();
        }
        else if(tecla == KeyEvent.VK_0)
        {
            btn_cero.doClick();
        }
        else if(tecla == '.')
        {
            btn_punto.doClick();
        }
        else{
            txt_calcu.setText("Invalido"); //Toda tecla que no sea numerica va a ser invalidada
        }
    }//GEN-LAST:event_txt_calcuKeyReleased

