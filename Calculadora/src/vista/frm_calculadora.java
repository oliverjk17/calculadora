    private void btn_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_igualActionPerformed
        // TODO add your handling code here:
        File archivo; //reg
        FileWriter escribir; //reg
        PrintWriter linea; //reg
        archivo = new File("bitacoraCalculadora.txt"); //reg
        AudioClip sound;
        sound = java.applet.Applet.newAudioClip(getClass().getResource("/vista/BEEP.wav"));
        sound.play();
        double procesond;
        if(operacion.equals("nula")){
            txt_calcu.setText(pantalla);
        }
        else if(operacion.equals("sumar")){
            procesond = Double.parseDouble(pantalla);
            resultado = proceso + procesond;
            txt_calcu.setText(String.format("% .2f", resultado));
            pantalla = String.valueOf(resultado);
            operacion = "nula";
            try{ //reg
                escribir = new FileWriter(archivo,true);
                linea = new PrintWriter(escribir);
                linea.print(String.valueOf(proceso)+"\n");
                linea.print("+\n");
                linea.print(String.valueOf(procesond)+"\n");
                linea.print(String.valueOf(resultado)+"\n");
                linea.close();
                escribir.close();
            }catch(IOException e){
                System.out.println("Error de archivo");
            }
        }
        else if(operacion.equals("restar")){
            procesond = Double.parseDouble(pantalla);
            resultado = proceso - procesond;
            txt_calcu.setText(String.format("% .2f", resultado));
            pantalla = String.valueOf(resultado);
            operacion = "nula";
            try{ //reg
                escribir = new FileWriter(archivo,true);
                linea = new PrintWriter(escribir);
                linea.print(String.valueOf(proceso)+"\n");
                linea.print("-\n");
                linea.print(String.valueOf(procesond)+"\n");
                linea.print(String.valueOf(resultado)+"\n");
                linea.close();
                escribir.close();
            }catch(IOException e){
                System.out.println("Error de archivo");
            }
        }
        else if(operacion.equals("multiplicar")){
            procesond = Double.parseDouble(pantalla);
            resultado = proceso * procesond;
            txt_calcu.setText(String.format("% .2f", resultado));
            pantalla = String.valueOf(resultado);
            operacion = "nula";
            try{ //reg
                escribir = new FileWriter(archivo,true);
                linea = new PrintWriter(escribir);
                linea.print(String.valueOf(proceso)+"\n");
                linea.print("*\n");
                linea.print(String.valueOf(procesond)+"\n");
                linea.print(String.valueOf(resultado)+"\n");
                linea.close();
                escribir.close();
            }catch(IOException e){
                System.out.println("Error de archivo");
            }
        }
        else if(operacion.equals("dividir")){
            procesond = Double.parseDouble(pantalla);
            if(procesond == 0){
                txt_calcu.setText("Invalido");
            }else{
                resultado = proceso / procesond; 
                txt_calcu.setText(String.format("% .2f", resultado));
                pantalla = String.valueOf(resultado);
                operacion = "nula";
                try{ //reg
                escribir = new FileWriter(archivo,true);
                linea = new PrintWriter(escribir);
                linea.print(String.valueOf(proceso)+"\n");
                linea.print("/\n");
                linea.print(String.valueOf(procesond)+"\n");
                linea.print(String.valueOf(resultado)+"\n");
                linea.close();
                escribir.close();
                }catch(IOException e){
                 System.out.println("Error de archivo");
                }
            }
        }
        
        
        lbl_proceso.setText("");
        activado = true;
    }//GEN-LAST:event_btn_igualActionPerformed

